package com.codetalksdna.CodingPrograms.TypeConversions;

public class ConvertLongToInt {
    public static void main(String[] args) {
        long a = 500000000l;
        long b = 23455l;
        int i = (int)a;
        int i1 = (int)b;
        System.out.println("Long first number, second number are"+" "+a+ ", "+b);
        System.out.println("Integer first number, second number are"+" "+i+ ", "+i1);
    }
}
