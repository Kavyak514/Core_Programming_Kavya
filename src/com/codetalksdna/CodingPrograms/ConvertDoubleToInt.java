package com.codetalksdna.CodingPrograms;

public class ConvertDoubleToInt {
    public static void main(String[] args) {
        double d = 40.5D;
        //convert double to int
        int i = (int)d;
        System.out.println("Double is" + " " + d);
        System.out.println("Integer is" + " " + i);
    }
}
